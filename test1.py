def get_computer_string(number):
    # Получаем последнюю цифру и последние две цифры числа
    last_digit = number % 10
    last_two_digits = number % 100  

    # Определяем нужное окончание для слова "компьютер"
    if 11 <= last_two_digits <= 19:  # число оканчивается на 11-19, слово получает окончание "ов" (например, 11 компьютеров, 13 компьютеров)
        ending = "ов"
    elif last_digit == 1:   # число оканчивается на 1, но не на 11, слово получает окончание " " (например, 21 компьютер, 41 компьютер).
        ending = ""
    elif 2 <= last_digit <= 4: #  число оканчивается на 2, 3 или 4, но не на 12, 13 или 14, слово получает окончание "а" (например, 22 компьютера, 34 компьютера).
        ending = "а"
    else:
        ending = "ов"  # В остальных случаях слово получает окончание "ов" (например, 25 компьютеров, 48 компьютеров).

    return f"{number} компьютер{ending}"

# Примеры использования:
print(get_computer_string(25))  # "25 компьютеров"
print(get_computer_string(41))  # "41 компьютер"
print(get_computer_string(1048))  # "1048 компьютеров"
